#!/usr/bin/env python3
"""Materialize a Hugging Face causal decoder as an RTLGen decoder contract.

The checked-in decoder datasets intentionally avoid storing large ONNX files.
This helper downloads/exports the requested checkpoint in the evaluator
workspace, writes the model/tokenizer manifests expected by the existing
decoder-quality harness, and leaves large generated files under gitignored
paths.
"""

from __future__ import annotations

import argparse
import json
import shutil
import sys
from pathlib import Path
from typing import Any


REPO_ROOT = Path(__file__).resolve().parents[2]
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))


JsonDict = dict[str, Any]


def _resolve(path: str | Path) -> Path:
    candidate = Path(path)
    if candidate.is_absolute():
        return candidate
    return REPO_ROOT / candidate


def _write_json(path: Path, payload: JsonDict) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, indent=2, sort_keys=True) + "\n", encoding="utf-8")


def _import_runtime() -> tuple[Any, Any, Any]:
    missing: list[str] = []
    try:
        import torch  # type: ignore
    except ModuleNotFoundError:
        torch = None
        missing.append("torch")
    try:
        from transformers import AutoModelForCausalLM, AutoTokenizer  # type: ignore
    except ModuleNotFoundError:
        AutoModelForCausalLM = None
        AutoTokenizer = None
        missing.append("transformers")
    try:
        import onnx  # noqa: F401  # type: ignore
    except ModuleNotFoundError:
        missing.append("onnx")
    if missing:
        raise SystemExit(
            "missing dependencies for Hugging Face decoder materialization: "
            + ", ".join(sorted(set(missing)))
            + ". Install torch, transformers, and onnx in the evaluator Python environment."
        )
    return torch, AutoModelForCausalLM, AutoTokenizer


def _special_tokens(tokenizer: Any) -> JsonDict:
    eos = tokenizer.eos_token or "<|endoftext|>"
    return {
        "bos": tokenizer.bos_token or eos,
        "eos": eos,
        "unk": tokenizer.unk_token or eos,
        "pad": tokenizer.pad_token or eos,
    }


def _copy_tokenizer_assets(*, tokenizer: Any, tokenizer_dir: Path, tokenizer_id: str, source_model_id: str) -> None:
    tokenizer_dir.mkdir(parents=True, exist_ok=True)
    tokenizer.save_pretrained(tokenizer_dir)
    manifest = {
        "version": 0.1,
        "tokenizer_id": tokenizer_id,
        "kind": "gpt2_bpe",
        "family": "gpt2_bpe",
        "status": "materialized_hf_decoder_tokenizer_active",
        "backend_interface": "decoder_tokenizer_v1",
        "vocab_json": "vocab.json",
        "merges_txt": "merges.txt",
        "tokenizer_json": "tokenizer.json",
        "tokenizer_config_json": "tokenizer_config.json",
        "special_tokens_map_json": "special_tokens_map.json",
        "special_tokens": _special_tokens(tokenizer),
        "unk_supported": True,
        "pretokenization": {
            "byte_level": True,
            "add_prefix_space": True,
        },
        "normalization": {
            "unicode": "identity",
            "lowercase": False,
        },
        "source_model_id": source_model_id,
        "notes": "Generated by npu/eval/materialize_hf_decoder_contract.py; tokenizer assets are intentionally gitignored.",
    }
    _write_json(tokenizer_dir / "manifest.json", manifest)


def _export_onnx(*, model: Any, config: Any, onnx_path: Path, opset: int) -> None:
    torch, _, _ = _import_runtime()

    class DecoderWithPast(torch.nn.Module):
        def __init__(self, wrapped: Any) -> None:
            super().__init__()
            self.wrapped = wrapped

        def forward(self, input_ids: Any, attention_mask: Any, position_ids: Any, *past_flat: Any) -> tuple[Any, ...]:
            past_key_values = []
            for layer_idx in range(int(config.n_layer)):
                past_key_values.append((past_flat[2 * layer_idx], past_flat[2 * layer_idx + 1]))
            outputs = self.wrapped(
                input_ids=input_ids,
                attention_mask=attention_mask,
                position_ids=position_ids,
                past_key_values=tuple(past_key_values),
                use_cache=True,
            )
            present_flat: list[Any] = []
            for key, value in outputs.past_key_values:
                present_flat.extend([key, value])
            return (outputs.logits, *present_flat)

    wrapper = DecoderWithPast(model).eval()
    batch_size = 1
    seq_len = 4
    past_len = 0
    n_layer = int(config.n_layer)
    n_head = int(config.n_head)
    n_embd = int(config.n_embd)
    head_dim = n_embd // n_head
    input_ids = torch.ones((batch_size, seq_len), dtype=torch.long)
    attention_mask = torch.ones((batch_size, seq_len), dtype=torch.long)
    position_ids = torch.arange(seq_len, dtype=torch.long).reshape(batch_size, seq_len)
    past = []
    for _ in range(n_layer):
        past.append(torch.zeros((batch_size, n_head, past_len, head_dim), dtype=torch.float32))
        past.append(torch.zeros((batch_size, n_head, past_len, head_dim), dtype=torch.float32))

    input_names = ["input_ids", "attention_mask", "position_ids"]
    for layer_idx in range(n_layer):
        input_names.extend([f"past_key_values.{layer_idx}.key", f"past_key_values.{layer_idx}.value"])
    output_names = ["logits"]
    for layer_idx in range(n_layer):
        output_names.extend([f"present.{layer_idx}.key", f"present.{layer_idx}.value"])
    dynamic_axes: dict[str, dict[int, str]] = {
        "input_ids": {0: "batch", 1: "sequence"},
        "attention_mask": {0: "batch", 1: "sequence"},
        "position_ids": {0: "batch", 1: "sequence"},
        "logits": {0: "batch", 1: "sequence"},
    }
    for layer_idx in range(n_layer):
        dynamic_axes[f"past_key_values.{layer_idx}.key"] = {0: "batch", 2: "past_sequence"}
        dynamic_axes[f"past_key_values.{layer_idx}.value"] = {0: "batch", 2: "past_sequence"}
        dynamic_axes[f"present.{layer_idx}.key"] = {0: "batch", 2: "total_sequence"}
        dynamic_axes[f"present.{layer_idx}.value"] = {0: "batch", 2: "total_sequence"}

    onnx_path.parent.mkdir(parents=True, exist_ok=True)
    with torch.no_grad():
        torch.onnx.export(
            wrapper,
            (input_ids, attention_mask, position_ids, *past),
            str(onnx_path),
            input_names=input_names,
            output_names=output_names,
            dynamic_axes=dynamic_axes,
            opset_version=opset,
            do_constant_folding=True,
            dynamo=False,
        )


def _write_contracts(*, model_id: str, source_model_id: str, model_dir: Path, tokenizer_dir: Path) -> None:
    model_rel = model_dir.relative_to(REPO_ROOT).as_posix()
    tokenizer_rel = tokenizer_dir.relative_to(REPO_ROOT).as_posix()
    binding = {
        "version": 0.1,
        "binding_id": f"{model_id}_reference_onnx_v1",
        "source_model_id": source_model_id,
        "onnx_model_path": f"{model_rel}/reference_onnx/model.onnx",
        "model_config_path": f"{model_rel}/reference_onnx/config.json",
        "input_names": ["input_ids", "attention_mask", "position_ids"],
        "output_name": "logits",
        "kv_cache_input_pattern": "past_key_values.{layer}.{key,value}",
        "kv_cache_output_pattern": "present.{layer}.{key,value}",
        "status": "materialized_hf_decoder_reference_active",
    }
    _write_json(model_dir / "reference_onnx_binding.json", binding)

    base_backend = {
        "backend_id": "command_json_v1",
        "equivalence_group": f"{model_id}_reference_onnx_v1",
        "input_name": "input_ids",
        "model_config_path": f"{model_rel}/reference_onnx/config.json",
        "onnx_model_path": f"{model_rel}/reference_onnx/model.onnx",
        "output_name": "logits",
        "topk": 16,
        "trace_output_patterns": ["present.*"],
    }
    reference = {
        **base_backend,
        "command": ["python3", "npu/eval/run_llm_decoder_onnx_reference.py"],
        "runtime_target": "cpu_reference",
    }
    candidate_exact = {
        **base_backend,
        "command": ["python3", "npu/eval/run_llm_decoder_onnx_candidate.py"],
        "runtime_target": "software_emulation",
        "candidate_semantics": "onnx_logits_fp_softmax_exact_norm_exact_prob_fp",
        "softmax_mode": "exact",
        "normalization_mode": "exact",
        "trace_tensor_quant_bits": 4,
    }
    candidate_approx = {
        **candidate_exact,
        "candidate_semantics": "onnx_logits_fp_softmax_approx_pwl_in_q4_w_q4_norm_recip_q10_prob_fp",
        "softmax_mode": "approx_pwl",
        "softmax_input_quant_bits": 4,
        "softmax_weight_quant_bits": 4,
        "normalization_mode": "reciprocal_quantized",
        "normalization_reciprocal_bits": 10,
    }
    contract = {
        "version": 0.1,
        "model_id": model_id,
        "status": "materialized_distilgpt2_exact_reference_active",
        "task": "greedy_next_token",
        "execution_backend": "decoder_backend_v1",
        "decoder_backend_interface": "v1",
        "source_model_id": source_model_id,
        "tokenizer_id": model_id,
        "tokenizer_manifest": f"{tokenizer_rel}/manifest.json",
        "exact_reference_binding": f"{model_rel}/reference_onnx_binding.json",
        "backend_configs": {
            "reference": reference,
            "candidate": candidate_exact,
        },
        "backend_templates": {
            "reference_onnx": reference,
            "candidate_onnx_softmax_exact": candidate_exact,
            "candidate_onnx_softmax_approx": candidate_approx,
        },
        "notes": (
            "Generated from a Hugging Face causal decoder by npu/eval/materialize_hf_decoder_contract.py. "
            "The ONNX model and tokenizer assets are evaluator-local generated artifacts and are gitignored."
        ),
    }
    _write_json(model_dir / "model_contract.json", contract)


def _write_dataset_manifest(*, dataset_dir: Path, model_dir: Path, tokenizer_dir: Path, model_id: str, sample_file: str) -> None:
    dataset_rel = dataset_dir.relative_to(REPO_ROOT).as_posix()
    model_rel = model_dir.relative_to(REPO_ROOT).as_posix()
    tokenizer_rel = tokenizer_dir.relative_to(REPO_ROOT).as_posix()
    manifest = {
        "version": 0.1,
        "dataset_id": "llm_decoder_eval_distilgpt2_trained_v1",
        "task": "greedy_next_token",
        "sample_count": 24,
        "sample_file": sample_file,
        "reference_manifest": f"{dataset_rel}/reference_manifest.json",
        "candidate_manifest": f"{dataset_rel}/candidate_manifest.json",
        "tokenizer_id": model_id,
        "tokenizer_manifest": f"{tokenizer_rel}/manifest.json",
        "model_contract": f"{model_rel}/model_contract.json",
        "decoder_backend_interface": "v1",
        "status": "materialized_distilgpt2_quality_manifest_v1",
        "notes": (
            "distilgpt2 trained-checkpoint confirmation dataset. Run materialize_hf_decoder_contract.py "
            "before generating reference/candidate manifests because the model/tokenizer artifacts are gitignored."
        ),
        "decoder_backend_configs": {
            "reference": {
                "topk": 16,
            },
            "candidate": {
                "topk": 16,
            },
        },
    }
    _write_json(dataset_dir / "manifest.json", manifest)


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--model-id", default="distilgpt2", help="Hugging Face model id to export")
    parser.add_argument("--contract-id", default="llm_decoder_distilgpt2_trained_v1")
    parser.add_argument("--model-dir", default="runs/models/llm_decoder_distilgpt2_trained_v1")
    parser.add_argument("--tokenizer-dir", default="runs/tokenizers/llm_decoder_distilgpt2_trained_v1")
    parser.add_argument("--dataset-dir", default="runs/datasets/llm_decoder_eval_distilgpt2_trained_v1")
    parser.add_argument("--sample-file", default="runs/datasets/llm_decoder_eval_distilgpt2_trained_v1/samples.jsonl")
    parser.add_argument("--opset", type=int, default=17)
    parser.add_argument("--force", action="store_true", help="Re-export even when model.onnx already exists")
    args = parser.parse_args()

    torch, AutoModelForCausalLM, AutoTokenizer = _import_runtime()
    model_dir = _resolve(args.model_dir)
    tokenizer_dir = _resolve(args.tokenizer_dir)
    dataset_dir = _resolve(args.dataset_dir)
    onnx_dir = model_dir / "reference_onnx"
    onnx_path = onnx_dir / "model.onnx"

    tokenizer = AutoTokenizer.from_pretrained(args.model_id)
    if tokenizer.pad_token is None:
        tokenizer.pad_token = tokenizer.eos_token
    _copy_tokenizer_assets(
        tokenizer=tokenizer,
        tokenizer_dir=tokenizer_dir,
        tokenizer_id=args.contract_id,
        source_model_id=args.model_id,
    )

    model = AutoModelForCausalLM.from_pretrained(args.model_id)
    model.eval()
    model.config.use_cache = True
    onnx_dir.mkdir(parents=True, exist_ok=True)
    model.config.to_json_file(onnx_dir / "config.json")
    if args.force or not onnx_path.exists():
        _export_onnx(model=model, config=model.config, onnx_path=onnx_path, opset=args.opset)
    else:
        print(f"reuse existing ONNX: {onnx_path}")
    shutil.copyfile(onnx_dir / "config.json", model_dir / "config.json")
    _write_contracts(
        model_id=args.contract_id,
        source_model_id=args.model_id,
        model_dir=model_dir,
        tokenizer_dir=tokenizer_dir,
    )
    _write_dataset_manifest(
        dataset_dir=dataset_dir,
        model_dir=model_dir,
        tokenizer_dir=tokenizer_dir,
        model_id=args.contract_id,
        sample_file=str(Path(args.sample_file).as_posix()),
    )
    print(
        json.dumps(
            {
                "model_contract": str((model_dir / "model_contract.json").relative_to(REPO_ROOT)),
                "tokenizer_manifest": str((tokenizer_dir / "manifest.json").relative_to(REPO_ROOT)),
                "dataset_manifest": str((dataset_dir / "manifest.json").relative_to(REPO_ROOT)),
                "onnx_model": str(onnx_path.relative_to(REPO_ROOT)),
                "torch_version": str(torch.__version__),
            },
            sort_keys=True,
        )
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
