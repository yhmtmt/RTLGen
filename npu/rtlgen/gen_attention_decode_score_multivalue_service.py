def _load(path: Path) -> JsonDict:
    payload = json.loads(path.read_text(encoding="utf-8"))
    if not isinstance(payload, dict):
        raise SystemExit(f"config must decode to a JSON object: {path}")
    return payload


def _sha256_text(text: str) -> str:
    return hashlib.sha256(text.encode("utf-8")).hexdigest()


def _sha256_file(path: Path) -> str:
    return hashlib.sha256(path.read_bytes()).hexdigest()


def _clog2(value: int) -> int:
    return max(1, math.ceil(math.log2(max(2, value))))


def _validate(config: JsonDict) -> JsonDict:
    top_name = str(config.get("top_name") or "").strip()
    body = config.get("attention_decode_score_multivalue_service")
    if not top_name or not isinstance(body, dict):
        raise SystemExit("config requires top_name and attention_decode_score_multivalue_service")

    cluster_count = int(body.get("cluster_count", 1))
    max_blocks = int(body.get("max_blocks", 16))
    packet_w = int(body.get("packet_w", 128))
    banks = int(body.get("banks", 2))
    req_queue_depth = int(body.get("req_queue_depth", 2))
    resp_queue_depth = int(body.get("resp_queue_depth", 2))
    bank_queue_depth = int(body.get("bank_queue_depth", 2))
    read_latency = int(body.get("read_latency", 1))
    arb_mode = str(body.get("arb_mode", "round_robin")).strip().lower()
    locality_burst_max = int(body.get("locality_burst_max", 2))
    score_scale_lanes_per_cycle = int(body.get("score_scale_lanes_per_cycle", 1))
    fsm_encoding = str(body.get("fsm_encoding", "default")).strip().lower()

    if cluster_count < 1 or cluster_count > 32:
        raise SystemExit("cluster_count must be in [1, 32]")
    if max_blocks < 8 or max_blocks > 16384 or (max_blocks & (max_blocks - 1)):
        raise SystemExit("max_blocks must be a power of two in [8, 16384]")
    if packet_w not in {128, 256}:
        raise SystemExit("packet_w must be 128 or 256")
    if banks < 1 or banks > 64:
        raise SystemExit("banks must be in [1, 64]")
    if req_queue_depth < 1 or resp_queue_depth < 1 or bank_queue_depth < 1:
        raise SystemExit("queue depths must be positive")
    if read_latency < 0 or read_latency > 64:
        raise SystemExit("read_latency must be in [0, 64]")
    if arb_mode not in {"round_robin", "locality_first_bounded"}:
        raise SystemExit("arb_mode must be round_robin or locality_first_bounded")
    if locality_burst_max < 1 or locality_burst_max > 64:
        raise SystemExit("locality_burst_max must be in [1, 64]")
    if score_scale_lanes_per_cycle not in {1, 2, 4, 8}:
        raise SystemExit("score_scale_lanes_per_cycle must be one of 1,2,4,8")
    if fsm_encoding not in {"default", "binary"}:
        raise SystemExit("fsm_encoding must be default or binary")

    return {
        "top_name": top_name,
        "cluster_count": cluster_count,
        "max_blocks": max_blocks,
        "packet_w": packet_w,
        "banks": banks,
        "req_queue_depth": req_queue_depth,
        "resp_queue_depth": resp_queue_depth,
        "bank_queue_depth": bank_queue_depth,
        "read_latency": read_latency,
        "arb_mode": arb_mode,
        "locality_burst_max": locality_burst_max,
        "score_scale_lanes_per_cycle": score_scale_lanes_per_cycle,
        "fsm_encoding": fsm_encoding,
    }


