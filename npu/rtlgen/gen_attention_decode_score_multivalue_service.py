def generate(config: JsonDict, out_dir: Path) -> None:
    params = _validate(config)
    top_name = str(params["top_name"])
    cluster_top = f"{top_name}__cluster"
    out_dir.mkdir(parents=True, exist_ok=True)
    with tempfile.TemporaryDirectory(prefix="multivalue-service-gen-") as tmp_text:
        tmp = Path(tmp_text)
        cluster_dir = tmp / "cluster"
        generate_cluster(
            {
                "top_name": cluster_top,
                "attention_decode_score_multivalue_cluster": {
                    "max_blocks": params["max_blocks"],
                    "array_n": 8,
                    "value_slices": 16,
                    "divider_impl": "iterative_restoring",
                    "score_scale_lanes_per_cycle": params["score_scale_lanes_per_cycle"],
                    "fsm_encoding": params["fsm_encoding"],
                },
            },
            cluster_dir,
        )
        cluster_text = (cluster_dir / "top.v").read_text(encoding="utf-8")
        cluster_manifest = json.loads(
            (cluster_dir / "attention_decode_score_multivalue_cluster_manifest.json").read_text(encoding="utf-8")
        )
        sim_rtl = [(REPO_ROOT / rel).read_text(encoding="utf-8") for rel in _SIM_RTL_PATHS]
        wrapper = _wrapper(top_name=top_name, params=params, cluster_top=cluster_top)

    top_text = "\n\n".join([cluster_text, *sim_rtl, wrapper]) + "\n"
    (out_dir / "top.v").write_text(top_text, encoding="utf-8")
    (out_dir / "config.json").write_text(json.dumps(config, indent=2, sort_keys=True) + "\n", encoding="utf-8")

    dependency_sources = [
        {
            "path": "npu/rtlgen/gen_attention_decode_score_multivalue_service.py",
            "sha256": _sha256_file(Path(__file__)),
        },
        {
            "path": "npu/rtlgen/gen_attention_decode_score_multivalue_cluster.py",
            "sha256": _sha256_file(REPO_ROOT / "npu/rtlgen/gen_attention_decode_score_multivalue_cluster.py"),
        },
        *[
            {
                "path": rel,
                "sha256": _sha256_file(REPO_ROOT / rel),
            }
            for rel in _SIM_RTL_PATHS
        ],
    ]
    manifest = {
        "version": 1,
        "generator": "npu/rtlgen/gen_attention_decode_score_multivalue_service.py",
        "top_name": top_name,
        "semantic_profile": "decode_m1x8_shared_score_16x8d_value_iterdiv_onchip_service_v1",
        "cluster_count": params["cluster_count"],
        "max_blocks": params["max_blocks"],
        "packet_w": params["packet_w"],
        "banks": params["banks"],
        "req_queue_depth": params["req_queue_depth"],
        "resp_queue_depth": params["resp_queue_depth"],
        "bank_queue_depth": params["bank_queue_depth"],
        "read_latency": params["read_latency"],
        "arb_mode": params["arb_mode"],
        "locality_burst_max": params["locality_burst_max"],
        "value_dimensions": 128,
        "value_slices": 16,
        "score_scale_lanes_per_cycle": params["score_scale_lanes_per_cycle"],
        "fsm_encoding": params["fsm_encoding"],
        "dependency_sources": dependency_sources,
        "generated_top_sha256": _sha256_text(top_text),
        "submodule_manifests": {
            "multivalue_cluster": cluster_manifest,
        },
    }
    (out_dir / "attention_decode_score_multivalue_service_manifest.json").write_text(
        json.dumps(manifest, indent=2, sort_keys=True) + "\n",
        encoding="utf-8",
    )


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--config", type=Path, required=True)
    parser.add_argument("--out", type=Path, required=True)
    args = parser.parse_args()
    generate(_load(args.config), args.out)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
